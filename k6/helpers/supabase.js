import http from "k6/http";
import { check } from "k6";
import { REST_URL, FUNCTIONS_URL, SUPABASE_ANON_KEY } from "../config/env.js";

// Expected statuses — tell k6 these aren't errors for http_req_failed metric
const EDGE_FN_EXPECTED = { responseCallback: http.expectedStatuses(200, 400) };
const JOIN_EXPECTED = { responseCallback: http.expectedStatuses(201, 409) };

/**
 * PostgREST request helper.
 */
export function restRequest(method, path, token, body, tagName) {
  const headers = {
    "Content-Type": "application/json",
    apikey: SUPABASE_ANON_KEY,
    Authorization: `Bearer ${token}`,
  };

  // For inserts, request the inserted row back
  if (method === "POST" && !path.includes("/rpc/")) {
    headers["Prefer"] = "return=representation";
  }

  const url = `${REST_URL}${path}`;
  const opts = { headers, tags: { name: tagName || path } };

  if (method === "GET") return http.get(url, opts);
  if (method === "POST") return http.post(url, JSON.stringify(body), opts);
  if (method === "PATCH") return http.patch(url, JSON.stringify(body), opts);
  if (method === "DELETE") return http.del(url, null, opts);
  return http.request(method, url, JSON.stringify(body), opts);
}

/**
 * Call an edge function.
 */
export function callFunction(name, token, body, tagName) {
  const res = http.post(
    `${FUNCTIONS_URL}/${name}`,
    JSON.stringify(body),
    {
      headers: {
        "Content-Type": "application/json",
        apikey: SUPABASE_ANON_KEY,
        Authorization: `Bearer ${token}`,
      },
      tags: { name: tagName || name },
      ...EDGE_FN_EXPECTED,
    }
  );
  return res;
}

/**
 * Call get_my_chats_dashboard RPC.
 * Requires p_user_id parameter.
 */
export function getDashboard(token, userId) {
  const res = restRequest("POST", "/rpc/get_my_chats_dashboard", token, {
    p_user_id: userId,
    p_language_code: "en",
  }, "dashboard");

  check(res, {
    "dashboard: status 200": (r) => r.status === 200,
  });

  return res;
}

/**
 * Create a public chat with k6 test prefix.
 */
export function createChat(token, chatName) {
  const res = restRequest("POST", "/chats", token, {
    name: chatName,
    initial_message: "Load test chat - auto generated by k6",
    access_method: "public",
    is_active: true,
  }, "create_chat");

  const ok = check(res, {
    "create_chat: status 201": (r) => r.status === 201,
    "create_chat: has id": (r) => {
      try { return !!JSON.parse(r.body)[0]?.id; }
      catch { return false; }
    },
  });

  if (!ok) return null;

  try {
    const data = JSON.parse(res.body);
    return data[0];
  } catch {
    return null;
  }
}

