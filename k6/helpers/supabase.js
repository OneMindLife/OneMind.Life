/**
 * Join a chat as a participant.
 * Includes user_id for RLS policy (requires user_id = auth.uid()).
 */
export function joinChat(token, chatId, displayName, userId) {
  const res = http.post(
    `${REST_URL}/participants`,
    JSON.stringify({
      chat_id: chatId,
      display_name: displayName,
      user_id: userId,
      status: "active",
    }),
    {
      headers: {
        "Content-Type": "application/json",
        apikey: SUPABASE_ANON_KEY,
        Authorization: `Bearer ${token}`,
        Prefer: "return=representation",
      },
      tags: { name: "join_chat" },
      ...JOIN_EXPECTED,
    }
  );

  check(res, {
    "join_chat: status 201 or 409": (r) => r.status === 201 || r.status === 409,
  });

  return res;
}

/**
 * Join a chat via SECURITY DEFINER RPC — bypasses per-row RLS.
 * Combines join + get_participant into one call. Idempotent.
 * Returns { id, display_name, status, is_host } or null.
 */
export function joinChatRpc(token, chatId, displayName) {
  const res = restRequest(
    "POST",
    "/rpc/join_chat_returning_participant",
    token,
    { p_chat_id: chatId, p_display_name: displayName },
    "join_chat"
  );

  const ok = check(res, {
    "join_chat: status 200": (r) => r.status === 200,
  });

  if (!ok) return null;

  try {
    const data = JSON.parse(res.body);
    return data && data.length > 0 ? data[0] : null;
  } catch {
    return null;
  }
}

